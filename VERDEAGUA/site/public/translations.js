// â”€â”€ Language detection & application â”€â”€
(function () {
    // Detect language: check localStorage first, then browser
    const stored = localStorage.getItem('va_lang');
    const browserLang = (navigator.language || navigator.userLanguage || 'es').slice(0, 2).toLowerCase();
    const supported = ['es', 'en', 'de', 'fr', 'it'];
    const lang = stored || (supported.includes(browserLang) ? browserLang : 'es');

    // Store detected language
    if (!stored) localStorage.setItem('va_lang', lang);

    // Set HTML lang attribute
    document.documentElement.lang = lang;

    // Apply translations to all elements with data-i18n
    function applyTranslations(language) {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[key] && translations[key][language]) {
                el.textContent = translations[key][language];
            }
        });
        // Handle placeholders
        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            if (translations[key] && translations[key][language]) {
                el.placeholder = translations[key][language];
            }
        });
        // Update active language in selector
        document.querySelectorAll('[data-lang-btn]').forEach(btn => {
            btn.classList.toggle('ring-2', btn.getAttribute('data-lang-btn') === language);
            btn.classList.toggle('ring-primary', btn.getAttribute('data-lang-btn') === language);
            btn.classList.toggle('opacity-60', btn.getAttribute('data-lang-btn') !== language);
        });
    }

    // Apply on DOM ready
    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', () => applyTranslations(lang));
    } else {
        applyTranslations(lang);
    }

    // Expose function for manual language switching
    window.switchLanguage = function (newLang) {
        localStorage.setItem('va_lang', newLang);
        applyTranslations(newLang);
        document.documentElement.lang = newLang;
    };

    // Expose current language
    window.currentLang = lang;
})();
